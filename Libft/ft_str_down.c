#include "Includes/libft.h"

void	ft_str_down(char *str)
{
	int		i;

	i = 0;
	while (str[i] && str)
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] += 32;
		i++;
	}
}
