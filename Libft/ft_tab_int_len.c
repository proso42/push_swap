#include "Includes/libft.h"

int		ft_tab_int_len(int *tab_int)
{
	int		i;
	int		*tmp;

	i = 0;
	tmp = tab_int;
	while (tmp != NULL)
	{
		tmp++;
		i++;
	}
	return (i);
}
