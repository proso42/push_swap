#include "Includes/libft.h"

void	ft_str_up(char *str)
{
	int		i;

	i = 0;
	while (str[i] && str)
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			str[i] -= 32;
		i++;
	}
}
